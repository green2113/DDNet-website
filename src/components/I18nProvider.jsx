function detectInitialLanguage() {
  if(typeof window === 'undefined') {
    return 'en';
  }

  const saved = window.localStorage.getItem(STORAGE_KEY);
  if(saved && LANGUAGE_OPTIONS.some((x) => x.code === saved)) {
    return saved;
  }
  return 'en';
}

function lookupText(language, key) {
  const chunks = String(key).split('.');
  let value = translations[language];
  for(const chunk of chunks) {
    if(value && typeof value === 'object' && chunk in value) {
      value = value[chunk];
    } else {
      return null;
    }
  }
  return typeof value === 'string' ? value : null;
}

function applyParams(text, params) {
  let output = text;
  for(const [key, value] of Object.entries(params || {})) {
    output = output.replaceAll(`{${key}}`, String(value));
  }
  return output;
}

const I18nContext = createContext(null);

export function I18nProvider({ children }) {
  const [language, setLanguageState] = useState(detectInitialLanguage);

  useEffect(() => {
    if(typeof window === 'undefined') {
      return;
    }
    const saved = window.localStorage.getItem(STORAGE_KEY);
    if(saved && LANGUAGE_OPTIONS.some((x) => x.code === saved)) {
      return;
    }

    let cancelled = false;
    const detectByCountry = async () => {
      try {
        const response = await fetch('/api/geo', { credentials: 'include' });
        const data = await response.json().catch(() => ({}));
        if(cancelled) {
          return;
        }
        const country = String(data?.country || '').toUpperCase();
        const next = LANGUAGE_BY_COUNTRY[country] || 'en';
        setLanguageState(next);
      } catch {
        if(!cancelled) {
          setLanguageState('en');
        }
      }
    };
    detectByCountry();

    return () => {
      cancelled = true;
    };
  }, []);

  const setLanguage = (next) => {
    if(!LANGUAGE_OPTIONS.some((x) => x.code === next)) return;
    setLanguageState(next);
    if(typeof window !== 'undefined') {
      window.localStorage.setItem(STORAGE_KEY, next);
    }
  };

  const t = (key, params = {}) => {
    const chosen = lookupText(language, key);
    if(chosen) {
      return applyParams(chosen, params);
    }
    const fallback = lookupText('en', key);
    return applyParams(fallback || key, params);
  };

  const value = useMemo(() => ({
    language,
    setLanguage,
    languages: LANGUAGE_OPTIONS,
    locale: LOCALE_BY_LANGUAGE[language] || 'en-US',
    t,
  }), [language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if(!ctx) {
    throw new Error('useI18n must be used inside I18nProvider');
  }
  return ctx;
}
